// Inicia as variáveis:
let celsius = 0
let farenheit = 0

celsius = parseFloat(prompt('Digite a temperatura em Celsius: '))

// Conversão de Celsius para Farenheit
farenheit = celsius * 1.8 + 32

alert('A temperatura em Farenheit é de: ' + farenheit.toFixed(2))

